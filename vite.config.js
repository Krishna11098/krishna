import { vitePlugin as remix } from "@remix-run/dev";
import { installGlobals } from "@remix-run/node";
import { defineConfig } from "vite";
import tsconfigPaths from "vite-tsconfig-paths";

// Install fetch and other globals used by Remix on the server
installGlobals({ nativeFetch: true });

// Handle legacy HOST env var for Shopify CLI compatibility
if (
  process.env.HOST &&
  (!process.env.SHOPIFY_APP_URL || process.env.SHOPIFY_APP_URL === process.env.HOST)
) {
  process.env.SHOPIFY_APP_URL = process.env.HOST;
  delete process.env.HOST;
}

// Extract hostname from the configured SHOPIFY_APP_URL or default to localhost
const host = new URL(process.env.SHOPIFY_APP_URL || "http://localhost").hostname;

// Support tunneling with localtunnel or ngrok by explicitly allowing the tunnel URL
const additionalAllowedHosts = [
  "localhost",
  host,
  "thirty-nails-feel.loca.lt" // âœ… Add your local tunnel host here
];

// Configure HMR settings depending on environment
let hmrConfig;

if (host === "localhost") {
  hmrConfig = {
    protocol: "ws",
    host: "localhost",
    port: 64999,
    clientPort: 64999,
  };
} else {
  hmrConfig = {
    protocol: "wss",
    host: host,
    port: parseInt(process.env.FRONTEND_PORT) || 8002,
    clientPort: 443,
  };
}

// Export Vite config
export default defineConfig({
  server: {
    allowedHosts: additionalAllowedHosts,
    cors: {
      preflightContinue: true,
    },
    port: Number(process.env.PORT || 3000),
    hmr: hmrConfig,
    fs: {
      allow: ["app", "node_modules"],
    },
  },
  plugins: [
    remix({
      ignoredRouteFiles: ["**/.*"],
      future: {
        v3_fetcherPersist: true,
        v3_relativeSplatPath: true,
        v3_throwAbortReason: true,
        v3_lazyRouteDiscovery: true,
        v3_singleFetch: false,
        v3_routeConfig: true,
      },
    }),
    tsconfigPaths(),
  ],
  build: {
    assetsInlineLimit: 0,
  },
  optimizeDeps: {
    include: ["@shopify/app-bridge-react", "@shopify/polaris"],
  },
});

